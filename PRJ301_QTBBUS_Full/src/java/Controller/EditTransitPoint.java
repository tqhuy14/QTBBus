    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        DAO dao = new DAO();
        int id = Integer.parseInt(request.getParameter("tID"));
        TransitPoint b = dao.getTransitPointByID(id);
        request.setAttribute("newt", b);
        if(b == null){
            response.sendRedirect(request.getContextPath()+"/manager_transitpoint");
        }else{
        request.getRequestDispatcher("EditTransitPoint.jsp").forward(request, response);
        }
    } 

